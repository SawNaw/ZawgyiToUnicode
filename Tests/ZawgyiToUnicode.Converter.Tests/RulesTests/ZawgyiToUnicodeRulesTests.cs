    [TestCase("(\u1031)?([\u1000-\u1021])(\u103b|\u103c)?\u108b", "\u1004\u103a\u1039$1$2$3\u102d")]
    [TestCase("(\u1031)?([\u1000-\u1021])(\u103b)?\u108c", "\u1004\u103a\u1039$1$2$3\u102e")]
    [TestCase("(\u1031)?([\u1000-\u1021])(\u103b)?\u108d", "\u1004\u103a\u1039$1$2$3\u1036")]
    [TestCase("\u108e", "\u102d\u1036")]
