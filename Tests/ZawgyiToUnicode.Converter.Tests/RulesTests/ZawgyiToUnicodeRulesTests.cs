    [TestCase("([\u1000-\u1021])\u103d\u1031\u103b", "$1\u103b\u103d\u1031")]
    [TestCase("([\u1000-\u1021])\u1031(\u1039[\u1000-\u1021])", "$1$2\u1031")]
    [TestCase("\u1038\u103a", "\u103a\u1038")]
    [TestCase("\u102d\u103a|\u103a\u102d", "\u102d")]
    [TestCase("\u102d\u102f\u103a", "\u102d\u102f")]
    [TestCase("\u0020\u1037", "\u1037")]
    [TestCase("\u1037\u1036", "\u1036\u1037")]
    [TestCase("[\u102d]+", "\u102d")]
    [TestCase("[\u103a]+", "\u103a")]
    [TestCase("[\u103d]+", "\u103d")]
    [TestCase("[\u1037]+", "\u1037")]
    [TestCase("[\u102e]+", "\u102e")]
    [TestCase("\u102d\u102e|\u102e\u102d", "\u102e")]
    [TestCase("\u102f\u102d", "\u102d\u102f")]
    [TestCase("\u1037\u1037", "\u1037")]
    [TestCase("\u1032\u1032", "\u1032")]
    [TestCase("\u1044\u1004\u103a\u1038", "\u104E\u1004\u103a\u1038")]
    [TestCase("([\u102d\u102e])\u1039([\u1000-\u1021])", "\u1039$2$1")]
    [TestCase("(\u103c\u1031)\u1039([\u1000-\u1021])", "\u1039$2$1")]
    [TestCase("\u1036\u103d", "\u103d\u1036")]
    [TestCase("\u1047((?=[\u1000-\u1021]\u103a)|(?=[\u102c-\u1030\u1032\u1036-\u1038\u103d\u103e]))", "\u101b")]
    public void ToUnicode_ReturnsUnicodeEquivalent(string zawgyi, string expectedUnicode)
    {
        Assert.That(Rules.ZawgyiToUnicodeRules[zawgyi], Is.EqualTo(expectedUnicode));
    }
